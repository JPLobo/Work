package arrays1;

public class APreviewPixel {
	
	private int red;
	private int green;
	private int blue;
	public APreviewPixel(int myRed, int myGreen, int myBlue)
	{
	red= myRed;
	green= myGreen;
	blue= myBlue;
	}
	public String toString()
	{
	return " ( " + red + " " + green + " "+ blue + ")";
	}
	}


